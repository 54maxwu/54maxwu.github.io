/* ============================================================
 * 主函式：以國曆生日計算
 * @return {digits, talent:[..], talentSum, main, master, grid:{1..9:count},
 *          lines:[{key,name,say}], missing:[{n,say}], flow:{year,num,say}}
 * ============================================================ */
export function lingshu(y, m, d, nowYear){
  const s = String(y).padStart(4,"0") + String(m).padStart(2,"0") + String(d).padStart(2,"0");
  const digits = s.split("").map(Number);
  let talentSum = digits.reduce((a,b)=>a+b,0);   // 第一次相加（通常兩位數）＝天賦數
  const master = (talentSum===11||talentSum===22||talentSum===33) ? talentSum : 0;
  let main = talentSum;
  while(main > 9) main = sumDigits(main);

  // 九宮格：生日 8 碼（去 0）＋天賦數兩碼（去 0）＋主命數
  const pool = [...digits, ...digitsOf(talentSum), main].filter(n=>n>0);
  const grid = {};
  for(let i=1;i<=9;i++) grid[i]=0;
  pool.forEach(n=>grid[n]++);

  const lines = LINES.filter(([k])=>k.split("").every(c=>grid[+c]>0))
                     .map(([key,name,say])=>({key,name,say}));
  const missing = Object.keys(grid).filter(k=>grid[k]===0).map(k=>({n:+k, say:MISS_SAY[k]}));

  // 流年數：當年西元＋生月＋生日 逐位相加 → 個位
  const ny = nowYear || new Date().getFullYear();
  let flow = sumDigits(ny) + sumDigits(m) + sumDigits(d);
  while(flow > 9) flow = sumDigits(flow);

  return {
    digits, talentSum, talent: digitsOf(talentSum).filter(n=>n>0),
    main, master, grid, lines, missing,
    flow:{ year: ny, num: flow, say: FLOW_SAY[flow] },
  };
}
