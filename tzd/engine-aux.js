/* 某字若改成 x 畫，重算該格數→是否吉。回傳建議的候選筆畫(就近 2-3 個)
 * favorWx: 喜用神五行陣列，會優先推薦「該筆畫五行＝喜用」的選項 */
function suggestStrokes(targetFn, curStroke, favorWx){
  const goods=luckyNumbers();
  const favor=new Set(favorWx||[]);
  const cand=[];
  for(let x=1;x<=24;x++){            // 名字單字常見 1~24 畫
    if(x===curStroke) continue;
    if(goods.includes(targetFn(x))) cand.push({stroke:x, grid:targetFn(x), wx:strokeWx(x), fav:favor.has(strokeWx(x))});
  }
  // 先排「符合喜用五行」者，再依與原筆畫的接近程度（改動越小越好寫）
  cand.sort((a,b)=>(b.fav-a.fav) || (Math.abs(a.stroke-curStroke)-Math.abs(b.stroke-curStroke)));
  return cand.slice(0,3);
}

/* 改名建議：針對「凶」的格，指出要改名字的哪個字、改成幾畫
 * 五格公式(單姓雙名)：天=姓+1、人=姓+名1、地=名1+名2、外=名2+1、總=姓+名1+名2
 */
export function nameSuggest(info, grids, favorWx){
  const n=info.length;
  if(n<2) return [];
  const s=info[0].n, m1=info[1].n, m2=(n>=3?info[2].n:null);
  const adv=[];
  grids.forEach(g=>{
    if(g.luck!==1) return;
    if(g.name==="天格"){
      adv.push({grid:"天格",fixable:false,note:"天格由「姓氏」筆畫決定，姓不能改；可改名字讓人格、地格、三才轉吉來補強。"});
      return;
    }
    let who="",cur=0,fn=null;
    if(g.name==="人格"){ who="名的第一個字"; cur=m1; fn=x=>s+x; }
    else if(g.name==="地格"){ if(m2==null){ who="名（單名）"; cur=m1; fn=x=>x+1; } else { who="名的第一個字"; cur=m1; fn=x=>x+m2; } }
    else if(g.name==="外格"){ if(m2==null){ adv.push({grid:"外格",fixable:false,note:"單名的外格較固定，建議改用雙名來調整外格。"}); return; } who="名的第二個字"; cur=m2; fn=x=>x+1; }
    else if(g.name==="總格"){ who=(m2!=null?"名的任一個字":"名字"); cur=(m2!=null?m2:m1); fn=(m2!=null?(x=>s+m1+x):(x=>s+x)); }
    if(fn){
      const cands=suggestStrokes(fn,cur,favorWx);
      adv.push({grid:g.name, fixable:cands.length>0, who, curStroke:cur, cands});
    }
  });
  return adv;
}

export function nameAnalysis(name){
  const chars=[...(name||"")].filter(c=>c.trim());
  if(chars.length===0) return null;
  const info=chars.map(c=>{const s=strokeOf(c); return {c,...s,wx:strokeWx(s.n)};});
  const sk=info.map(i=>i.n);
  const total=sk.reduce((a,b)=>a+b,0);
  const hasEstimate=info.some(i=>i.est);

  // 五格（單姓單名/雙名通用法）
  let tian,ren,di,wai;
  if(chars.length===1){            // 單字名罕見，補1
    tian=sk[0]+1; ren=sk[0]+1; di=1+1; wai=2;
  } else if(chars.length===2){     // 單姓單名
    tian=sk[0]+1; ren=sk[0]+sk[1]; di=sk[1]+1; wai=2;
  } else {                          // 單姓雙名（最常見）或更多
    tian=sk[0]+1;
    ren=sk[0]+sk[1];
    di=sk[1]+sk[2];
    wai=Math.max(1, total - ren + 1);
  }
  const grids=[
    {name:"天格",num:tian,wx:strokeWx(tian),luck:luck81(tian),meaning:"祖蔭根基，先天賦予"},
    {name:"人格",num:ren,wx:strokeWx(ren),luck:luck81(ren),meaning:"一生主運，性格核心"},
    {name:"地格",num:di,wx:strokeWx(di),luck:luck81(di),meaning:"前半生運、根基與子女緣"},
    {name:"外格",num:wai,wx:strokeWx(wai),luck:luck81(wai),meaning:"社交人際、外在助力"},
    {name:"總格",num:total,wx:strokeWx(total),luck:luck81(total),meaning:"中晚年運、人生總成"}
  ];
  // 三才 = 天人地的五行
  const sancai=sancaiLuck(strokeWx(tian),strokeWx(ren),strokeWx(di));
  // 分數：五格吉凶平均
  const score=Math.round(grids.reduce((a,g)=>a+g.luck,0)/grids.length/3*100);

  return {
    chars:info, total, grids,
    sancai:{ wx:[strokeWx(tian),strokeWx(ren),strokeWx(di)], ...sancai },
    nameWx:info.map(i=>i.wx),
    score, hasEstimate
  };
}
