/* 通用逐年分析：給某領域 + 該年資料，產生白話 verdict
 * item: {year, age, stars:[流曜名], sihuaOnPalace:[祿/權...], majorStars:[本命該宮主星]}
 */
export function analyzeDomainYear(domainKey, item) {
  const D = DOMAINS[domainKey]; if (!D) return null;
  const { year, age, stars = [], sihuaOnPalace = [], majorStars = [] } = item;
  const good = stars.filter(s => D.good.includes(s) || FLOW_GOOD.includes(s));
  const bad = stars.filter(s => D.bad.includes(s) || FLOW_BAD.includes(s));
  const starSay = majorStars.map(n => D.starYear[n]).filter(Boolean)[0] || "";
  const sihuaSay = sihuaOnPalace.map(l => D.sihua[l]).filter(Boolean);
  let score = good.length - bad.length;
  sihuaSay.forEach(s => score += s.good ? 1 : -1);
  let tone, hi;
  if (score >= 2) { tone = "good"; hi = 3; }
  else if (score >= 1) { tone = "ok"; hi = 2; }
  else if (score === 0) { tone = "mid"; hi = 1; }
  else { tone = "bad"; hi = 0; }
  const bits = [];
  if (good.length) bits.push(`${D.goodWord}（${good.join("、")}）進入，<b>${D.goodEffect}</b>`);
  if (bad.length) bits.push(`煞星（${bad.join("、")}）進入，<b>${D.badEffect}</b>`);
  sihuaSay.forEach(s => bits.push(s.say));
  if (starSay) bits.push(starSay);
  if (!bits.length) bits.push(D.empty);
  return { year, age, tone, headline: D.headlines[hi], text: bits.join("；").replace(/。$/, "") + "。" };
}

/* 疊宮自動分析：流年某宮 疊在 本命某宮 → 給一句白話提醒
 * yl: 流年 layout {map: branch->{name}}; z: 本命 {palaces[]}
 * 回傳重點疊宮（流年命/財/官/夫妻 疊到的本命宮）陣列 */
const PALACE_SHORT_LOCAL = { 命宮: "個性與整體運", 兄弟: "手足人際", 夫妻: "感情婚姻", 子女: "子女與創意", 財帛: "財運", 疾厄: "健康", 遷移: "外出際遇", 僕役: "交友人脈", 官祿: "事業", 田宅: "家庭不動產", 福德: "心境福分", 父母: "父母長輩" };
export function detectDieGong(yl, z) {
  if (!yl || !yl.map || !z || !z.palaces) return [];
  // 本命：地支 -> 本命宮名
  const benByBranch = {};
  z.palaces.forEach(p => { if (p.earthlyBranch) benByBranch[p.earthlyBranch] = p.name; });
  const focus = ["命宮", "財帛", "官祿", "夫妻"];
  const out = [];
  Object.keys(yl.map).forEach(branch => {
    const yname = yl.map[branch].name;
    if (!focus.includes(yname)) return;
    const benName = benByBranch[branch];
    if (!benName || benName === yname) return; // 疊在同名宮不特別提
    out.push({
      yname, benName,
      say: `今年的<b>流年${yname}</b>正好疊在你的<b>本命${benName}宮</b>上——代表今年「${PALACE_SHORT_LOCAL[yname] || yname}」這塊，會和你先天的「${PALACE_SHORT_LOCAL[benName] || benName}」緊密牽動、互相影響。`
    });
  });
  // 命宮疊宮排最前
  return out.sort((a, b) => (a.yname === "命宮" ? -1 : 0) - (b.yname === "命宮" ? -1 : 0));
}
