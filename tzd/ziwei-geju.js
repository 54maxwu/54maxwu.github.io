/* 偵測：回傳命中的格局（含完整資料），依類別輕重排序展示 */
export function detectGeju(z) {
  if (!z || !z.palaces) return [];
  const hit = [];
  for (const g of GEJU) {
    if (typeof g.match === "function") {
      let ok = false;
      try { ok = !!g.match(z); } catch (e) { ok = false; }
      if (ok) hit.push(g);
    }
  }
  // 警示格排後面，其餘維持資料順序
  return hit.sort((a, b) => (a.cat === "警示" ? 1 : 0) - (b.cat === "警示" ? 1 : 0));
}

