/* ---- 偵測小工具 ---- */
// 取某宮（依宮名）
const byName = (z, n) => (z.palaces || []).find(p => p.name === n) || null;
// 取某地支的宮
const byBranch = (z, b) => (z.palaces || []).find(p => p.earthlyBranch === b) || null;
// 宮內主星名陣列
const majorsOf = p => (p && p.majorStars ? p.majorStars.map(s => s.name) : []);
// 全盤輔星/雜曜名（含 minor）
const minorsOf = p => (p && p.minorStars ? p.minorStars.map(s => s.name) : []);
// 某宮是否含某些主星（全部都要）
const hasMajors = (p, arr) => { const m = majorsOf(p); return arr.every(s => m.includes(s)); };
// 命宮地支
const mingBranch = z => { const m = byName(z, "命宮"); return m ? m.earthlyBranch : ""; };
// 命宮主星
const mingMajors = z => majorsOf(byName(z, "命宮"));
// 某宮是否坐某輔星（六吉/六煞等在 minorStars）
const minorIn = (p, name) => minorsOf(p).includes(name);
// 「夾」：命宮左右兩宮（兄弟宮、父母宮）是否各坐一顆指定星
const jiaMing = (z, a, b) => {
  const xiong = byName(z, "兄弟"), fu = byName(z, "父母");
  if (!xiong || !fu) return false;
  const L = minorsOf(xiong).concat(majorsOf(xiong));
  const R = minorsOf(fu).concat(majorsOf(fu));
  return (L.includes(a) && R.includes(b)) || (L.includes(b) && R.includes(a));
};
// 命宮兩側是否被指定四化夾（兄弟/父母宮主星帶該 mutagen）
const mutsAround = (z) => {
  const out = [];
  ["兄弟", "父母"].forEach(n => {
    const p = byName(z, n);
    (p && p.majorStars || []).forEach(s => { if (s.mutagen) out.push(s.mutagen); });
  });
  return out;
};
// 命宮主星帶某化
const mingMutagen = (z, mu) => (byName(z, "命宮")?.majorStars || []).some(s => s.mutagen === mu);

