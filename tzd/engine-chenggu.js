/* 把 0.x 兩 → 「幾錢」中文 */
const QIAN_CN=["","一","二","三","四","五","六","七","八","九"];
const LIANG_CN=["零","一","二","三","四","五","六","七","八","九"];
function weightCN(w){
  const liang=Math.floor(w+1e-6);
  const qian=Math.round((w-liang)*10);
  let s=(LIANG_CN[liang]||liang)+"兩";
  if(qian>0) s+=QIAN_CN[qian]+"錢";
  return s;
}

/* 主函式：傳入 bz（buildBazi 結果）與農曆 {m,d}，回傳稱骨結果 */
export function chengGu(bz, lunar){
  const yearGZ = bz.yGan + bz.yZhi;
  const yW = YEAR_W[yearGZ] ?? 0;
  const mW = MONTH_W[lunar.m] ?? 0;
  const dW = DAY_W[lunar.d] ?? 0;
  const hourKnown = bz.hourKnown;
  const hW = hourKnown ? (HOUR_W[bz.hZhi] ?? 0) : 0;

  const total = Math.round((yW + mW + dW + hW) * 100) / 100;
  // 取 POEMS 的鍵（一位小數），不足/超界時夾到表內範圍
  let key = total.toFixed(1);
  if(!POEMS[key]){
    const keys=Object.keys(POEMS).map(Number).sort((a,b)=>a-b);
    const clamped=Math.min(Math.max(total,keys[0]),keys[keys.length-1]);
    // 找最接近的鍵
    key=keys.reduce((best,k)=>Math.abs(k-clamped)<Math.abs(best-clamped)?k:best,keys[0]).toFixed(1);
  }
  const poem = POEMS[key] || [];

  return {
    yearGZ,
    parts:[
      {label:"出生年", sub:yearGZ, w:yW, cn:weightCN(yW)},
      {label:"出生月", sub:`農曆${lunar.m}月`, w:mW, cn:weightCN(mW)},
      {label:"出生日", sub:`農曆${lunar.d}日`, w:dW, cn:weightCN(dW)},
      {label:"出生時", sub:hourKnown?`${bz.hZhi}時`:"未填時辰", w:hW, cn:hourKnown?weightCN(hW):"—"}
    ],
    hourKnown,
    total,
    totalCN: weightCN(total),
    poem,
    poemTitle: weightCN(total)+"的命"
  };
}
